package com.example.beneficiary.model;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.index.Indexed;
import org.springframework.data.mongodb.core.mapping.Document;

/**
 * Models the data used for Beneficiaries.
 * 
 * @author fernando.ocampo
 */
@Document(collection = "beneficiaries")
public class Beneficiary {
    @Id
    private String id;
    @Indexed(unique = true)
    private String code;
    private String forename;
    private String lastname;
    private String relationship;
    private String affiliateId;
    private Integer state;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getForename() {
        return forename;
    }

    public void setForename(String forename) {
        this.forename = forename;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getRelationship() {
        return relationship;
    }

    public void setRelationship(String relationship) {
        this.relationship = relationship;
    }

    public String getAffiliateId() {
        return affiliateId;
    }

    public void setAffiliateId(String affiliateId) {
        this.affiliateId = affiliateId;
    }

    public Integer getState() {
        return state;
    }

    public void setState(Integer state) {
        this.state = state;
    }

    @Override
    public String toString() {
        return "{" + "id=" + id + ", code=" + code + ", forename=" + forename + ", lastname=" + lastname + ", relationship=" + relationship + ", affiliateId=" + affiliateId + '}';
    }
}
